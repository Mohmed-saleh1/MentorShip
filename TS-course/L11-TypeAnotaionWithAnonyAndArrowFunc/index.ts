/**
 * Function 
 * .. Anonymous function
 * .. arrow function
 */

const addWithAnony = function(num1:number,num2:number):number{
    return num1+num2;
}
console.log(addWithAnony(4,5));

const addWithArrow = (num1:number,num2:number):number =>{
    return num1+num2
}

console.log(addWithArrow(4,5));

